#!/usr/bin/env bash

# Reset
NC='\033[0m'           # Text Reset

# Regular Colors
RED='\033[0;31m'          # Red
GREEN='\033[0;32m'        # Green
YELLOW='\033[0;33m'       # Yellow
BLUE='\033[0;34m'         # Blue
PURPLE='\033[0;35m'       # Purple
CYAN='\033[0;36m'         # Cyan
WHITE='\033[0;37m'        # White

LINE="=================================================================="

GLINE="${GREEN}${LINE}${NC}"
RLINE="${RED}${LINE}${NC}"